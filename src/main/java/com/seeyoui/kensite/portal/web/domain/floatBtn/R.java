public class R {
	private String t;//下标
	private String lt;//左上
	private String rt;//右上
	private String lb;//左下
	private String rb;//右下
	public String getT() {
		return t;
	}
	public void setT(String t) {
		this.t = t;
	}
	public String getLt() {
		return lt;
	}
	public void setLt(String lt) {
		this.lt = lt;
	}
	public String getRt() {
		return rt;
	}
	public void setRt(String rt) {
		this.rt = rt;
	}
	public String getLb() {
		return lb;
	}
	public void setLb(String lb) {
		this.lb = lb;
	}
	public String getRb() {
		return rb;
	}
	public void setRb(String rb) {
		this.rb = rb;
	}
	
}
