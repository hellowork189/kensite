package com.seeyoui.kensite.portal.web.domain.floatBtn;

public class SH {
	private String t;//下标
	private String dx;//距离X
	private String dy;//距离Y
	private String f;//模糊
	private String c;//颜色
	public String getT() {
		return t;
	}
	public void setT(String t) {
		this.t = t;
	}
	public String getDx() {
		return dx;
	}
	public void setDx(String dx) {
		this.dx = dx;
	}
	public String getDy() {
		return dy;
	}
	public void setDy(String dy) {
		this.dy = dy;
	}
	public String getF() {
		return f;
	}
	public void setF(String f) {
		this.f = f;
	}
	public String getC() {
		return c;
	}
	public void setC(String c) {
		this.c = c;
	}
	
}
